#include "libft.h"

int	ft_strchr_idx(const char *s, char c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			break ;
		i++;
	}
	if (s[i] == '\0')
		return (-1);
	return (i);
}
